// IsAttached returns true if TV is attached to an external tela process.
func (a *App) IsAttached() bool {
	a.mu.Lock()
	defer a.mu.Unlock()
	return a.attached
}

// waitForProcessExit polls until the tela process is no longer tracked,
// or until the timeout expires.
func (a *App) waitForProcessExit(timeout time.Duration) {
	deadline := time.Now().Add(timeout)
	for time.Now().Before(deadline) {
		a.mu.Lock()
		proc := a.telaProcess
		conn := a.connected
		a.mu.Unlock()
		if proc == nil && !conn {
			return
		}
		time.Sleep(100 * time.Millisecond)
	}
}

// --- Helpers ---

func toWSURL(hubURL string) string {
	if strings.HasPrefix(hubURL, "https://") {
		return "wss://" + strings.TrimPrefix(hubURL, "https://")
	}
	if strings.HasPrefix(hubURL, "http://") {
		return "ws://" + strings.TrimPrefix(hubURL, "http://")
	}
	if !strings.HasPrefix(hubURL, "wss://") && !strings.HasPrefix(hubURL, "ws://") {
		return "wss://" + hubURL
	}
	return hubURL
}
