// ── Mount (WebDAV) child process management ──────────────────────

// StartMount launches "tela mount" as a child process and streams its output.
// It reads mount config from the current profile.
func (a *App) StartMount() (string, error) {
	a.mu.Lock()
	if a.mountProcess != nil {
		a.mu.Unlock()
		return "", fmt.Errorf("mount is already running")
	}
	a.mu.Unlock()

	telaPath := a.findTool("tela")
	if telaPath == "" {
		return "", fmt.Errorf("tela binary not found")
	}

	mc := a.GetMountConfig()
	args := []string{"mount"}
	if mc.Port > 0 {
		args = append(args, "-port", fmt.Sprintf("%d", mc.Port))
	}
	if mc.Mount != "" {
		args = append(args, "-mount", mc.Mount)
	}

	cmd := exec.Command(telaPath, args...)
	hideConsoleWindow(cmd)

	outPipe, _ := cmd.StdoutPipe()
	cmd.Stderr = cmd.Stdout

	if err := cmd.Start(); err != nil {
		return "", fmt.Errorf("failed to start mount: %w", err)
	}

	pid := cmd.Process.Pid

	a.mu.Lock()
	a.mountProcess = cmd.Process
	a.mu.Unlock()

	a.logCommand("Start mount", telaPath+" "+strings.Join(args, " "))

	go func() {
		buf := make([]byte, 4096)
		for {
			n, err := outPipe.Read(buf)
			if n > 0 {
				chunk := string(buf[:n])
				if a.ctx != nil {
					wailsRuntime.EventsEmit(a.ctx, "tela:output", chunk)
				}
			}
			if err != nil {
				break
			}
		}
		cmd.Wait()
		a.mu.Lock()
		a.mountProcess = nil
		a.mu.Unlock()
		if a.ctx != nil {
			wailsRuntime.EventsEmit(a.ctx, "tela:output", "[mount] stopped\n")
		}
	}()

	return fmt.Sprintf("mount started (pid %d)", pid), nil
}

// StopMount stops the mount child process.
func (a *App) StopMount() error {
	// Unmap the drive before killing the process to prevent orphaned mappings
	mc := a.GetMountConfig()
	platformUnmountDrive(mc.Mount)

	a.mu.Lock()
	defer a.mu.Unlock()
	if a.mountProcess == nil {
		return nil
	}
	killProcessTree(a.mountProcess.Pid)
	a.mountProcess.Kill()
	a.mountProcess = nil
	return nil
}

// IsMountRunning returns true if mount was launched by TV and is still running.
func (a *App) IsMountRunning() bool {
	// Check if TV launched the mount process
	a.mu.Lock()
	tvManaged := a.mountProcess != nil
	a.mu.Unlock()
	if tvManaged {
		return true
	}

	// Check if mount is running externally (e.g., auto-mount from tela connect)
	// by probing the WebDAV port
	mc := a.GetMountConfig()
	port := mc.Port
	if port == 0 {
		port = 18080
	}
	conn, err := net.DialTimeout("tcp", fmt.Sprintf("127.0.0.1:%d", port), 500*time.Millisecond)
	if err != nil {
		return false
	}
	conn.Close()
	return true
}

