// ConfirmDisconnect shows a native confirmation dialog if connected
// and the confirmDisconnect setting is enabled.
// Returns true if not connected, confirmation is disabled, or user confirms.
func (a *App) ConfirmDisconnect() bool {
	if !a.IsConnected() {
		return true
	}
	a.mu.Lock()
	isAttached := a.attached
	a.mu.Unlock()

	// Attached mode: no confirmation needed (detach is non-destructive)
	if isAttached {
		return true
	}

	s := a.GetSettings()
	if !s.ConfirmDisconnect {
		return true
	}
	result, _ := wailsRuntime.MessageDialog(a.ctx, wailsRuntime.MessageDialogOptions{
		Type:          wailsRuntime.QuestionDialog,
		Title:         "Disconnect",
		Message:       "Are you sure you want to disconnect?",
		DefaultButton: "No",
		Buttons:       []string{"Yes", "No"},
	})
	return result == "Yes"
}

// ConfirmDialog shows a native confirmation dialog and returns the user's choice.
func (a *App) ConfirmDialog(title, message string) bool {
	result, _ := wailsRuntime.MessageDialog(a.ctx, wailsRuntime.MessageDialogOptions{
		Type:          wailsRuntime.QuestionDialog,
		Title:         title,
		Message:       message,
		DefaultButton: "No",
		Buttons:       []string{"Yes", "No"},
	})
	return result == "Yes"
}

