// GetMachineCapabilities returns a map of machineID -> capabilities
// by querying the hub status API for each hub in the current profile.
// Uses the stored token if available, but also tries without auth
// (works for open-mode hubs and viewer tokens delivered via cookie).
func (a *App) GetMachineCapabilities() map[string]map[string]interface{} {
	result := make(map[string]map[string]interface{})

	data, err := os.ReadFile(profilePath())
	if err != nil {
		return result
	}
	var profile Profile
	if yaml.Unmarshal(data, &profile) != nil {
		return result
	}

	hubsSeen := map[string]bool{}
	for _, conn := range profile.Connections {
		hubURL := conn.Hub
		if hubsSeen[hubURL] {
			continue
		}
		hubsSeen[hubURL] = true

		// Build the HTTPS URL for the status API
		apiURL := strings.Replace(hubURL, "wss://", "https://", 1)
		apiURL = strings.Replace(apiURL, "ws://", "http://", 1)
		apiURL = strings.TrimSuffix(apiURL, "/") + "/api/status"

		req, err := http.NewRequest("GET", apiURL, nil)
		if err != nil {
			continue
		}

		// Add auth token if available
		token := credstore.LookupToken(toWSURL(hubURL))
		if token != "" {
			req.Header.Set("Authorization", "Bearer "+token)
		}

		resp, err := a.doRequest(req, 10*time.Second)
		if err != nil {
			continue
		}
		respBody, err := io.ReadAll(resp.Body)
		resp.Body.Close()
		if err != nil {
			continue
		}

		var status struct {
			Machines []struct {
				ID           string                 `json:"id"`
				Capabilities map[string]interface{} `json:"capabilities"`
			} `json:"machines"`
		}
		if json.Unmarshal(respBody, &status) != nil {
			continue
		}
		for _, m := range status.Machines {
			if m.Capabilities != nil {
				result[m.ID] = m.Capabilities
			}
		}
	}
	return result
}

// controlEndpoint reads the control API info and returns (base URL, token).
func controlEndpoint() (string, string, error) {
	controlPath := filepath.Join(telaConfigDir(), "run", "control.json")
	data, err := os.ReadFile(controlPath)
	if err != nil {
		return "", "", fmt.Errorf("no active tela connection")
	}
	var info struct {
		Port  int    `json:"port"`
		Token string `json:"token"`
	}
	if json.Unmarshal(data, &info) != nil || info.Port == 0 {
		return "", "", fmt.Errorf("invalid control file")
	}
	return fmt.Sprintf("http://127.0.0.1:%d", info.Port), info.Token, nil
}

// FileShareRequest sends a file share operation to a connected machine
// via the tela control API's /files proxy.
func (a *App) FileShareRequest(machine string, opJSON string) string {

	base, token, err := controlEndpoint()
	if err != nil {
		return errJSON(err.Error())
	}

	req, _ := http.NewRequest("POST", base+"/files/"+machine, strings.NewReader(opJSON+"\n"))
	req.Header.Set("Authorization", "Bearer "+token)
	req.Header.Set("Content-Type", "application/json")

	resp, err := a.doRequestLogged(req, 30*time.Second)
	if err != nil {
		return errJSON("request failed: " + err.Error())
	}
	defer resp.Body.Close()

	body, _ := io.ReadAll(resp.Body)

	// Error responses from the proxy use pretty-printed JSON (multiline).
	// Parse them as a whole object rather than splitting on newlines.
	if resp.StatusCode != http.StatusOK {
		var errResp struct {
			Error string `json:"error"`
		}
		if json.Unmarshal(body, &errResp) == nil && errResp.Error != "" {
			return errJSON(errResp.Error)
		}
		return errJSON(fmt.Sprintf("HTTP %d", resp.StatusCode))
	}

	// Success: the file share protocol uses single-line JSON responses
	// (possibly followed by chunk data for reads). Take the first line.
	lines := strings.SplitN(string(body), "\n", 2)
	if len(lines) > 0 {
		return lines[0]
	}
	return errJSON("empty response")
}

// FileShareDownload downloads a file from a connected machine's file share.
// Returns the local path where the file was saved, or an error string.
func (a *App) FileShareDownload(machine string, share string, remotePath string, localPath string) string {
	// HTTP logging handled by doRequest

	base, token, err := controlEndpoint()
	if err != nil {
		return errJSON(err.Error())
	}

	opJSON := fmt.Sprintf(`{"op":"read","share":%s,"path":%s}`, jsonString(share), jsonString(remotePath))
	req, _ := http.NewRequest("POST", base+"/files/"+machine, strings.NewReader(opJSON+"\n"))
	req.Header.Set("Authorization", "Bearer "+token)

	resp, err := a.doRequestLogged(req, 300*time.Second)
	if err != nil {
		return errJSON("request failed: " + err.Error())
	}
	defer resp.Body.Close()

	respBody, err := io.ReadAll(resp.Body)
	if err != nil {
		return errJSON("read response failed: " + err.Error())
	}

	reader := bufio.NewReader(bytes.NewReader(respBody))

	// Read JSON response header
	headerLine, err := reader.ReadBytes('\n')
	if err != nil {
		return errJSON("read header failed: " + err.Error())
	}
	var fsResp struct {
		OK       bool   `json:"ok"`
		Error    string `json:"error"`
		Size     int64  `json:"size"`
		Checksum string `json:"checksum"`
	}
	if err := json.Unmarshal(headerLine, &fsResp); err != nil {
		return errJSON("invalid response: " + string(headerLine))
	}
	if !fsResp.OK {
		return errJSON(fsResp.Error)
	}

	// Create local file
	f, err := os.Create(localPath)
	if err != nil {
		return errJSON("cannot create file: " + err.Error())
	}
	defer f.Close()

	// Read chunks with checksum verification
	h := sha256.New()
	var totalReceived int64
	for {
		line, err := reader.ReadString('\n')
		if err != nil {
			os.Remove(localPath)
			return errJSON(fmt.Sprintf("chunk read error after %d bytes: %v", totalReceived, err))
		}
		line = strings.TrimRight(line, "\n\r")
		if !strings.HasPrefix(line, "CHUNK ") {
			os.Remove(localPath)
			return errJSON(fmt.Sprintf("protocol error after %d bytes: expected CHUNK, got %q", totalReceived, line))
		}
		chunkSize, _ := strconv.ParseInt(strings.TrimPrefix(line, "CHUNK "), 10, 64)
		if chunkSize == 0 {
			break
		}
		n, err := io.CopyN(io.MultiWriter(f, h), reader, chunkSize)
		if err != nil || n != chunkSize {
			os.Remove(localPath)
			return errJSON(fmt.Sprintf("incomplete chunk: wanted %d, got %d: %v", chunkSize, n, err))
		}
		totalReceived += n
	}

	// Verify checksum
	if fsResp.Checksum != "" {
		computed := "sha256:" + hex.EncodeToString(h.Sum(nil))
		if computed != fsResp.Checksum {
			os.Remove(localPath)
			return errJSON("checksum mismatch (file deleted)")
		}
	}

	return fmt.Sprintf(`{"ok":true,"size":%d,"path":%s}`, totalReceived, jsonString(localPath))
}

// FileShareUpload uploads a local file to a connected machine's file share.
func (a *App) FileShareUpload(machine string, share string, localPath string, remoteName string) string {
	// HTTP logging handled by doRequest

	base, token, err := controlEndpoint()
	if err != nil {
		return errJSON(err.Error())
	}

	info, err := os.Stat(localPath)
	if err != nil {
		return errJSON(err.Error())
	}

	// The control API /files proxy expects the full protocol exchange in
	// the request body. We build: JSON header + chunks + CHUNK 0.
	pr, pw := io.Pipe()
	go func() {
		defer pw.Close()

		// Compute checksum
		f, err := os.Open(localPath)
		if err != nil {
			pw.CloseWithError(err)
			return
		}
		h := sha256.New()
		io.Copy(h, f)
		checksum := "sha256:" + hex.EncodeToString(h.Sum(nil))
		f.Seek(0, io.SeekStart)

		// Write request header
		header := fmt.Sprintf(`{"op":"write","share":%s,"path":%s,"size":%d,"checksum":"%s"}`+"\n",
			jsonString(share), jsonString(remoteName), info.Size(), checksum)
		pw.Write([]byte(header))

		// Write chunks
		buf := make([]byte, 16384)
		for {
			n, err := f.Read(buf)
			if n > 0 {
				pw.Write([]byte(fmt.Sprintf("CHUNK %d\n", n)))
				pw.Write(buf[:n])
			}
			if err != nil {
				break
			}
		}
		pw.Write([]byte("CHUNK 0\n"))
		f.Close()
	}()

	req, _ := http.NewRequest("POST", base+"/files/"+machine, pr)
	req.Header.Set("Authorization", "Bearer "+token)

	resp, err := a.doRequestLogged(req, 300*time.Second)
	if err != nil {
		return errJSON("upload failed: " + err.Error())
	}
	defer resp.Body.Close()

	body, _ := io.ReadAll(resp.Body)

	if resp.StatusCode != http.StatusOK {
		var errResp struct {
			Error string `json:"error"`
		}
		if json.Unmarshal(body, &errResp) == nil && errResp.Error != "" {
			return errJSON(errResp.Error)
		}
		return errJSON(fmt.Sprintf("HTTP %d", resp.StatusCode))
	}

	lines := strings.SplitN(string(body), "\n", 2)
	if len(lines) > 0 {
		return lines[0]
	}
	return errJSON("empty response")
}

