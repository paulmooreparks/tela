// doRequest executes an HTTP request with the given timeout using the shared client.
// The context is kept alive until the response body is fully read. Callers receive
// a response whose Body is safe to read without context cancellation races.
func (a *App) doRequest(req *http.Request, timeout time.Duration) (*http.Response, error) {
	ctx, cancel := context.WithTimeout(req.Context(), timeout)
	resp, err := a.httpClient.Do(req.WithContext(ctx))
	if err != nil {
		cancel()
		return nil, err
	}
	// Read the entire body while the context is still alive, then replace
	// resp.Body with an in-memory reader so the caller can read it safely.
	body, err := io.ReadAll(resp.Body)
	resp.Body.Close()
	cancel()
	if err != nil {
		return nil, err
	}
	resp.Body = io.NopCloser(bytes.NewReader(body))
	return resp, nil
}

// NewApp creates a new App instance.
func NewApp() *App {
	jar, _ := cookiejar.New(nil)
	return &App{
		httpClient: &http.Client{
			Jar: jar,
			// No global Timeout -- per-request context timeouts are used
			// via doRequest(). A global timeout would override context
			// deadlines and kill long-running file transfers.
		},
		assignedPorts: make(map[int]bool),
	}
}

func (a *App) startup(ctx context.Context) {
	a.ctx = ctx

	// Bring the embedded portal up first so the rest of TV (and any
	// future Wails-bound function that wants to call into it) can
	// rely on its endpoint file existing. The portal is the source
	// of truth for hub directory, fleet aggregation, and the admin
	// proxy in the new architecture; today nothing in TV calls into
	// it yet, but bringing it up at startup is the foundation for
	// the Infrastructure mode rewire.
	if pe, err := startEmbeddedPortal(ctx); err != nil {
		log.Printf("[telavisor] embedded portal: failed to start: %v", err)
	} else {
		a.portal = pe
	}

	// Apply any pending self-update from a previous run
	a.applyPendingSelfUpdate()

	// Set up system tray icon
	a.setupTray()

	// Apply startup settings
	settings := a.GetSettings()

	// Set default profile: use setting, or find first available profile
	profileMu.Lock()
	if settings.DefaultProfile != "" {
		currentProfileName = settings.DefaultProfile
	} else {
		// Check if default profile exists, otherwise use first available
		defaultPath := filepath.Join(profilesDir(), currentProfileName+".yaml")
		if _, err := os.Stat(defaultPath); os.IsNotExist(err) {
			entries, _ := os.ReadDir(profilesDir())
			for _, e := range entries {
				if !e.IsDir() && filepath.Ext(e.Name()) == ".yaml" {
					currentProfileName = strings.TrimSuffix(e.Name(), ".yaml")
					break
				}
			}
		}
	}
	profileMu.Unlock()

	// Restore window position and size
	if settings.WindowSaved {
		if settings.WindowWidth > 0 && settings.WindowHeight > 0 {
			wailsRuntime.WindowSetSize(ctx, settings.WindowWidth, settings.WindowHeight)
		}
		wailsRuntime.WindowSetPosition(ctx, settings.WindowX, settings.WindowY)
	}

	// Check for updates
	if settings.AutoCheckUpdates {
		go a.checkForUpdates()
	}

}

// checkForUpdates checks if any managed binary (telavisor or tela CLI) is
// behind the latest GitHub release. If so, sets updatePending but does NOT
// auto-download. The user clicks "Restart to Update" to trigger the update.
// Exception: if tela CLI is not installed at all, it is downloaded immediately.
func (a *App) checkForUpdates() {
	latest, err := a.latestRelease()
	if err != nil {
		return // offline
	}

	needsUpdate := false

	// Check telavisor version
	if version != latest {
		needsUpdate = true
	}

	// Check tela CLI version
	bin := "tela"
	if runtime.GOOS == "windows" {
		bin += ".exe"
	}
	localPath := filepath.Join(telaInstallDir(), bin)

	if _, err := os.Stat(localPath); err != nil {
		// tela not installed -- download it now (first run)
		a.installTool("tela", latest)
	} else {
		// tela exists -- check version
		cmd := exec.Command(localPath, "version")
		hideConsoleWindow(cmd)
		out, _ := cmd.CombinedOutput()
		parts := strings.Fields(strings.TrimSpace(string(out)))
		if len(parts) >= 2 && parts[1] != latest {
			needsUpdate = true
		}
	}

	if needsUpdate {
		a.mu.Lock()
		a.updatePending = true
		a.updateVersion = latest
		a.mu.Unlock()
	}
}

// isPackageManaged returns true if telavisor was installed via a package manager
// (deb, rpm, msi, dmg). In that case, self-update is disabled and the user
// should update via their package manager.
func isPackageManaged() bool {
	exe := selfExePath()
	if exe == "" {
		return false
	}
	// Linux: installed to /usr/bin or /usr/local/bin by deb/rpm
	if strings.HasPrefix(exe, "/usr/bin/") || strings.HasPrefix(exe, "/usr/local/bin/") {
		return true
	}
	// macOS: installed to /Applications
	if strings.HasPrefix(exe, "/Applications/") {
		return true
	}
	// Windows: installed to Program Files
	pf := os.Getenv("ProgramFiles")
	pfx86 := os.Getenv("ProgramFiles(x86)")
	if pf != "" && strings.HasPrefix(strings.ToLower(exe), strings.ToLower(pf)) {
		return true
	}
	if pfx86 != "" && strings.HasPrefix(strings.ToLower(exe), strings.ToLower(pfx86)) {
		return true
	}
	return false
}

// selfExePath returns the absolute path of the currently running telavisor binary.
func selfExePath() string {
	exe, err := os.Executable()
	if err != nil {
		return ""
	}
	exe, _ = filepath.Abs(exe)
	return exe
}

// selfUpdatePath returns the staging path for a pending update (next to the running binary).
func selfUpdatePath() string {
	exe := selfExePath()
	if exe == "" {
		return ""
	}
	ext := filepath.Ext(exe)
	base := strings.TrimSuffix(exe, ext)
	return base + "-update" + ext
}

// applyPendingSelfUpdate checks for a staged update binary and swaps it in place.
func (a *App) applyPendingSelfUpdate() {
	updatePath := selfUpdatePath()
	if updatePath == "" {
		return
	}
	if _, err := os.Stat(updatePath); err != nil {
		return // no pending update
	}

	currentPath := selfExePath()
	if currentPath == "" {
		return
	}

	if runtime.GOOS == "windows" {
		// Can't overwrite running exe -- rename current to .old first
		oldPath := currentPath + ".old"
		os.Remove(oldPath)
		if err := os.Rename(currentPath, oldPath); err != nil {
			log.Printf("[update] failed to rename current binary: %v", err)
			return
		}
		if err := os.Rename(updatePath, currentPath); err != nil {
			// Rollback
			os.Rename(oldPath, currentPath)
			log.Printf("[update] failed to install update: %v", err)
			return
		}
		// Clean up old binary with retry (may be locked briefly)
		go func() {
			for range 10 {
				if os.Remove(oldPath) == nil {
					return
				}
				time.Sleep(500 * time.Millisecond)
			}
		}()
	} else {
		// Unix: just rename over the running binary
		if err := os.Rename(updatePath, currentPath); err != nil {
			log.Printf("[update] failed to install update: %v", err)
			return
		}
		os.Chmod(currentPath, 0755)
	}

	log.Printf("[update] applied pending update")
}

