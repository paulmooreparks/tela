// GetKnownHubs returns all hubs the user has configured locally.
// Reads from credentials.yaml and hubs.yaml.
func (a *App) GetKnownHubs() []KnownHub {
	seen := make(map[string]bool)
	var hubs []KnownHub

	// 1. Credential store -- hubs with stored tokens
	store, err := credstore.Load(credstore.UserPath())
	if err == nil && store != nil {
		for url, cred := range store.Hubs {
			name := hubNameFromURL(url)
			hubs = append(hubs, KnownHub{
				Name:     name,
				URL:      url,
				HasToken: cred.Token != "",
				Source:   "credentials",
			})
			seen[url] = true
		}
	}

	// 2. Hub aliases -- short names mapped to URLs
	hubsFile := filepath.Join(telaConfigDir(), "hubs.yaml")
	if data, err := os.ReadFile(hubsFile); err == nil {
		var cfg struct {
			Hubs map[string]string `yaml:"hubs"`
		}
		if yaml.Unmarshal(data, &cfg) == nil {
			for name, url := range cfg.Hubs {
				if !seen[url] {
					hasToken := false
					if store != nil {
						_, hasToken = store.Hubs[url]
					}
					hubs = append(hubs, KnownHub{
						Name:     name,
						URL:      url,
						HasToken: hasToken,
						Source:   "hubs",
					})
					seen[url] = true
				}
			}
		}
	}

	return hubs
}

func hubNameFromURL(url string) string {
	url = strings.TrimPrefix(url, "wss://")
	url = strings.TrimPrefix(url, "ws://")
	url = strings.TrimPrefix(url, "https://")
	url = strings.TrimPrefix(url, "http://")
	url = strings.TrimSuffix(url, "/")
	return url
}

// GetHubStatus queries a hub for its machine/service status.
func (a *App) GetHubStatus(hubURL string) HubStatus {
	token := credstore.LookupToken(hubURL)
	if token == "" {
		return HubStatus{Error: "no token stored for this hub"}
	}

	// Convert wss:// to https:// for the API call
	apiURL := hubURL
	apiURL = strings.Replace(apiURL, "wss://", "https://", 1)
	apiURL = strings.Replace(apiURL, "ws://", "http://", 1)
	apiURL = strings.TrimSuffix(apiURL, "/") + "/api/status"

	req, err := http.NewRequest("GET", apiURL, nil)
	if err != nil {
		return HubStatus{Error: err.Error()}
	}
	req.Header.Set("Authorization", "Bearer "+token)

	resp, err := a.httpClient.Do(req)
	if err != nil {
		return HubStatus{Error: err.Error()}
	}
	defer resp.Body.Close()

	if resp.StatusCode != 200 {
		return HubStatus{Error: fmt.Sprintf("HTTP %d", resp.StatusCode)}
	}

	var raw struct {
		HubName  string `json:"hubName"`
		Machines []struct {
			ID             string `json:"id"`
			Hostname       string `json:"hostname"`
			OS             string `json:"os"`
			AgentConnected bool   `json:"agentConnected"`
			SessionCount   int    `json:"sessionCount"`
			LastSeen       string `json:"lastSeen"`
			Services       []struct {
				Name  string `json:"name"`
				Port  int    `json:"port"`
				Proto string `json:"proto"`
			} `json:"services"`
		} `json:"machines"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&raw); err != nil {
		return HubStatus{Error: err.Error()}
	}

	status := HubStatus{
		Online:  true,
		HubName: raw.HubName,
	}
	for _, m := range raw.Machines {
		ms := MachineStatus{
			ID:             m.ID,
			Hostname:       m.Hostname,
			OS:             m.OS,
			AgentConnected: m.AgentConnected,
			SessionCount:   m.SessionCount,
			LastSeen:       m.LastSeen,
		}
		for _, s := range m.Services {
			ms.Services = append(ms.Services, ServiceInfo{
				Name:  s.Name,
				Port:  s.Port,
				Proto: s.Proto,
			})
		}
		status.Machines = append(status.Machines, ms)
	}
	return status
}

// --- Hub Management ---

// AddHub stores a hub URL and token in the credential store.
func (a *App) AddHub(url, token string) error {
	wsURL := toWSURL(url)
	a.logCommand("Add hub "+wsURL, "tela login "+wsURL)

	store, err := credstore.Load(credstore.UserPath())
	if err != nil {
		store = &credstore.Store{Hubs: make(map[string]credstore.Credential)}
	}
	store.Set(wsURL, credstore.Credential{Token: token})
	return store.Save(credstore.UserPath())
}

// RemoveHub removes a hub from the credential store.
func (a *App) RemoveHub(url string) error {
	wsURL := toWSURL(url)
	a.logCommand("Remove hub "+wsURL, "tela logout "+wsURL)

	// Remove from credential store
	store, err := credstore.Load(credstore.UserPath())
	if err == nil {
		store.Remove(wsURL)
		store.Save(credstore.UserPath())
	}

	// Remove from hubs.yaml aliases
	hubsFile := filepath.Join(telaConfigDir(), "hubs.yaml")
	if data, err := os.ReadFile(hubsFile); err == nil {
		var cfg struct {
			Hubs map[string]string `yaml:"hubs"`
		}
		if yaml.Unmarshal(data, &cfg) == nil && cfg.Hubs != nil {
			changed := false
			for name, hubURL := range cfg.Hubs {
				if hubURL == url || hubURL == wsURL {
					delete(cfg.Hubs, name)
					changed = true
				}
			}
			if changed {
				out, _ := yaml.Marshal(&cfg)
				os.WriteFile(hubsFile, out, 0600)
			}
		}
	}

	return nil
}

// GetStoredToken checks the credential store for a token for the given hub URL.
func (a *App) GetStoredToken(hubURL string) string {
	return credstore.LookupToken(toWSURL(hubURL))
}

// --- Docker Integration ---

// DockerGetToken runs telahubd user show-owner inside a Docker container.
func (a *App) DockerGetToken(container, role string) (string, error) {
	if role == "" {
		role = "show-owner"
	}
	cmd := fmt.Sprintf("docker exec %s telahubd user %s -config /app/data/telahubd.yaml",
		container, role)
	a.logCommand("Get "+role+" token from container "+container, cmd)

	out, err := exec.Command("docker", "exec", container,
		"telahubd", "user", role, "-config", "/app/data/telahubd.yaml").CombinedOutput()
	if err != nil {
		return "", fmt.Errorf("%s: %s", err, strings.TrimSpace(string(out)))
	}
	return strings.TrimSpace(string(out)), nil
}

// DockerListContainers lists running Docker containers (for hub discovery).
func (a *App) DockerListContainers() ([]string, error) {
	out, err := exec.Command("docker", "ps", "--format", "{{.Names}}").CombinedOutput()
	if err != nil {
		return nil, fmt.Errorf("docker not available: %s", strings.TrimSpace(string(out)))
	}
	lines := strings.Split(strings.TrimSpace(string(out)), "\n")
	var names []string
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line != "" {
			names = append(names, line)
		}
	}
	return names, nil
}

// --- Profile-Based Connection ---

// AssignLocalPort picks a unique local port for a service. Checks system
// availability AND avoids ports already assigned to other selected services.
func (a *App) AssignLocalPort(servicePort int) int {
	a.mu.Lock()
	defer a.mu.Unlock()

	// Try the advertised port first
	if !a.assignedPorts[servicePort] && isPortAvailable(servicePort) {
		a.assignedPorts[servicePort] = true
		return servicePort
	}

	// Try port + 10000, then increment until we find one
	candidate := servicePort + 10000
	for i := 0; i < 100; i++ {
		if !a.assignedPorts[candidate] && isPortAvailable(candidate) {
			a.assignedPorts[candidate] = true
			return candidate
		}
		candidate++
	}

	// Last resort
	a.assignedPorts[servicePort] = true
	return servicePort
}

// PortRequest is a service that needs a local port assigned.
type PortRequest struct {
	Key         string `json:"key"`         // unique key like "hub||machine||service"
	ServicePort int    `json:"servicePort"` // the advertised remote port
}

// PortAssignment is the result of port resolution.
type PortAssignment struct {
	Key       string `json:"key"`
	LocalPort int    `json:"localPort"`
}

// ResolveAllPorts takes all selected services and assigns clash-free local ports.
// Called whenever selections change. Resets all previous assignments.
func (a *App) ResolveAllPorts(requestsJSON string) ([]PortAssignment, error) {
	var requests []PortRequest
	if err := json.Unmarshal([]byte(requestsJSON), &requests); err != nil {
		return nil, fmt.Errorf("invalid requests: %w", err)
	}

	a.mu.Lock()
	defer a.mu.Unlock()

	// Reset all assignments
	a.assignedPorts = make(map[int]bool)

	var results []PortAssignment
	for _, req := range requests {
		port := req.ServicePort

		// Try the advertised port
		if !a.assignedPorts[port] && isPortAvailable(port) {
			a.assignedPorts[port] = true
			results = append(results, PortAssignment{Key: req.Key, LocalPort: port})
			continue
		}

		// Try port + 10000, then increment
		candidate := port + 10000
		found := false
		for i := 0; i < 100; i++ {
			if !a.assignedPorts[candidate] && isPortAvailable(candidate) {
				a.assignedPorts[candidate] = true
				results = append(results, PortAssignment{Key: req.Key, LocalPort: candidate})
				found = true
				break
			}
			candidate++
		}
		if !found {
			// Last resort -- use the original port
			a.assignedPorts[port] = true
			results = append(results, PortAssignment{Key: req.Key, LocalPort: port})
		}
	}

	return results, nil
}

// ReleaseLocalPort frees a previously assigned port.
func (a *App) ReleaseLocalPort(port int) {
	a.mu.Lock()
	defer a.mu.Unlock()
	delete(a.assignedPorts, port)
}

func isPortAvailable(port int) bool {
	// Check both loopback and all interfaces (Docker binds on 0.0.0.0)
	for _, addr := range []string{"127.0.0.1", "0.0.0.0"} {
		ln, err := net.Listen("tcp", fmt.Sprintf("%s:%d", addr, port))
		if err != nil {
			return false
		}
		ln.Close()
	}
	return true
}

