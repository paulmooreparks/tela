// AddHub adds a hub to the active portal source's directory. The
// hub is identified by URL and admin token; the portal store mints a
// short name from the URL host (or the user can rename it later).
// This replaces the older credstore-based AddHub: hub admin tokens
// now live exclusively in the portal store, never in
// ~/.tela/credentials.yaml.
func (a *App) AddHub(rawURL, token string) error {
	rawURL = strings.TrimSpace(rawURL)
	if rawURL == "" {
		return fmt.Errorf("AddHub: URL is required")
	}
	hubURL := canonicalHubURL(rawURL)
	name := hostFromURL(hubURL)
	a.logCommand("Add hub "+hubURL, "tela login "+hubURL)

	// Probe the hub's /api/status to capture its hubId. Best-effort:
	// if the probe fails (hub offline, auth required, pre-identity
	// hub) we still add the entry without a hubId.
	hubID := probeHubID(hubURL, token)

	sourceName, err := a.firstEnabledSourceName()
	if err != nil {
		return err
	}
	client, err := a.portalClientForSource(sourceName)
	if err != nil {
		return err
	}
	ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()
	_, err = client.AddHub(ctx, portalclient.AddHubRequest{
		Name:       name,
		URL:        hubURL,
		AdminToken: token,
		HubID:      hubID,
	})
	return err
}

// probeHubID queries a hub's /api/status and returns its hubId.
// Returns empty string on any failure (hub offline, no auth, pre-
// identity hub). Callers should treat an empty return as "unknown"
// and not block on it.
func probeHubID(hubURL, token string) string {
	ctx, cancel := context.WithTimeout(context.Background(), 5*time.Second)
	defer cancel()
	req, err := http.NewRequestWithContext(ctx, http.MethodGet, hubURL+"/api/status", nil)
	if err != nil {
		return ""
	}
	if token != "" {
		req.Header.Set("Authorization", "Bearer "+token)
	}
	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return ""
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		return ""
	}
	var status struct {
		HubID string `json:"hubId"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&status); err != nil {
		return ""
	}
	return status.HubID
}

// resolveHubNameByURL finds the portal-registry name for a hub given
// its URL (e.g. "wss://owlsnest.parkscomputing.com" → "owlsnest.parkscomputing.com").
// Used to bridge between code paths that hold a hub URL (control
// files, telad.yaml) and the admin proxy which keys hubs by name.
// Queries the first enabled portal source. Returns an error if no
// enabled source knows a hub at that URL.
func (a *App) resolveHubNameByURL(rawURL string) (string, error) {
	rawURL = strings.TrimSpace(rawURL)
	if rawURL == "" {
		return "", fmt.Errorf("empty hub URL")
	}
	canonical := canonicalHubURL(rawURL)
	sourceName, err := a.firstEnabledSourceName()
	if err != nil {
		return "", err
	}
	client, err := a.portalClientForSource(sourceName)
	if err != nil {
		return "", err
	}
	ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()
	hubs, err := client.ListHubs(ctx)
	if err != nil {
		return "", err
	}
	for _, h := range hubs {
		if canonicalHubURL(h.URL) == canonical {
			return h.Name, nil
		}
	}
	return "", fmt.Errorf("no hub at %s in portal source %q", canonical, sourceName)
}

// RemoveHub removes a hub from the active portal source's directory.
// Accepts either a hub name or a URL the frontend may still hand in
// (legacy code paths in the dashboard pass hub.url); URLs are
// canonicalized and matched against the directory.
func (a *App) RemoveHub(hubNameOrURL string) error {
	hubNameOrURL = strings.TrimSpace(hubNameOrURL)
	if hubNameOrURL == "" {
		return fmt.Errorf("RemoveHub: hub name or URL is required")
	}
	a.logCommand("Remove hub "+hubNameOrURL, "tela logout "+hubNameOrURL)

	sourceName, err := a.firstEnabledSourceName()
	if err != nil {
		return err
	}
	client, err := a.portalClientForSource(sourceName)
	if err != nil {
		return err
	}
	ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()

	// Resolve to a hub name. If the input is already a name in the
	// directory, use it as-is; otherwise look up by URL.
	target := hubNameOrURL
	hubs, err := client.ListHubs(ctx)
	if err != nil {
		return err
	}
	canonical := canonicalHubURL(hubNameOrURL)
	found := false
	for _, h := range hubs {
		if h.Name == hubNameOrURL || h.URL == canonical {
			target = h.Name
			found = true
			break
		}
	}
	if !found {
		return fmt.Errorf("RemoveHub: %q not found in portal source %q", hubNameOrURL, sourceName)
	}
	_, err = client.DeleteHub(ctx, target)
	return err
}

