// GetUpdateInfo returns all update state in one call, avoiding nested
// async calls on the frontend.
func (a *App) GetUpdateInfo() UpdateInfo {
	a.mu.Lock()
	pending := a.updatePending
	ver := a.updateVersion
	a.mu.Unlock()

	tv := a.GetToolVersions()

	return UpdateInfo{
		Pending:        pending,
		Version:        ver,
		GUI:            tv.GUI,
		CLI:            tv.CLI,
		GUIBehind:      tv.GUIBehind,
		CLIBehind:      tv.CLIBehind,
		PackageManaged: isPackageManaged(),
	}
}

// RestartToUpdate downloads updates for all binaries and relaunches.
func (a *App) RestartToUpdate() error {
	a.mu.Lock()
	ver := a.updateVersion
	a.mu.Unlock()

	if ver == "" {
		return fmt.Errorf("no update available")
	}

	// Disconnect if connected and wait for process to exit
	a.Disconnect()
	a.waitForProcessExit(5 * time.Second)

	// Update tela CLI
	a.installTool("tela", ver)

	if isPackageManaged() {
		// Package-managed: CLI updated, GUI must be updated via package manager
		a.mu.Lock()
		a.updatePending = false
		a.mu.Unlock()
		return nil
	}

	// Download telavisor update to staging path (next to running binary)
	a.downloadSelfUpdate(ver)

	exe := selfExePath()
	if exe == "" {
		return fmt.Errorf("cannot determine executable path")
	}

	// Apply the update now before relaunching.
	a.applyPendingSelfUpdate()

	// Relaunch from the same path (which is now the new binary)
	cmd := exec.Command(exe)
	cmd.Start()

	os.Exit(0)
	return nil // unreachable
}

// downloadSelfUpdate downloads the telavisor binary for the current platform.
func (a *App) downloadSelfUpdate(ver string) {
	ext := ""
	if runtime.GOOS == "windows" {
		ext = ".exe"
	}
	binaryName := fmt.Sprintf("telavisor-%s-%s%s", runtime.GOOS, runtime.GOARCH, ext)
	url := fmt.Sprintf("https://github.com/paulmooreparks/tela/releases/download/%s/%s", ver, binaryName)

	os.MkdirAll(telaInstallDir(), 0755)
	updatePath := selfUpdatePath()

	resp, err := http.Get(url)
	if err != nil || resp.StatusCode != 200 {
		if resp != nil {
			resp.Body.Close()
		}
		return
	}
	defer resp.Body.Close()

	tmpPath := updatePath + ".tmp"
	f, err := os.Create(tmpPath)
	if err != nil {
		return
	}
	if _, err := io.Copy(f, resp.Body); err != nil {
		f.Close()
		os.Remove(tmpPath)
		return
	}
	f.Close()

	os.Remove(updatePath)
	os.Rename(tmpPath, updatePath)

	if runtime.GOOS != "windows" {
		os.Chmod(updatePath, 0755)
	}
}

func (a *App) shutdown(ctx context.Context) {
	a.DisconnectControlWS()

	a.mu.Lock()
	if a.telaProcess != nil {
		killProcessTree(a.telaProcess.Pid)
		a.telaProcess = nil
		a.connected = false
	}
	a.mu.Unlock()

	// Force exit after timeout if goroutines are stuck.
	// Wails expects OnShutdown to return promptly. If the WebSocket
	// reader or output pipe goroutine is still blocking, this ensures
	// the process doesn't hang indefinitely.
	go func() {
		time.Sleep(3 * time.Second)
		os.Exit(0)
	}()
}

