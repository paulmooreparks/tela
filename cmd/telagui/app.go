// App is the main application struct exposed to the frontend via Wails bindings.
type App struct {
	ctx context.Context

	mu            sync.Mutex
	cmdLog        []CommandLogEntry
	httpClient    *http.Client
	assignedPorts map[int]bool // ports already assigned in this session

	// Profile-based connection state
	telaProcess *os.Process
	telaOutput  string
	connected   bool
	attached    bool // true when attached to external tela (not launched by TV)

	// mount child process (tela mount)
	mountProcess *os.Process

	// Self-update state
	updatePending bool
	updateVersion string

	// Window state
	quitting  bool
	modalOpen bool // set by JS via SetModalOpen; TDL rule: modals capture window chrome

	// WebSocket control connection
	controlWS *websocket.Conn

	// Embedded portal goroutine. Started at App.startup, stopped at
	// App.shutdown. See cmd/telagui/portal_embed.go and the project
	// memory project_tv_portal_architecture.md for the rationale.
	portal *portalEmbed
}

