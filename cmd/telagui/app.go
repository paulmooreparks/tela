// RestartToUpdate downloads updates for all binaries and relaunches.
func (a *App) RestartToUpdate() error {
	a.mu.Lock()
	ver := a.updateVersion
	a.mu.Unlock()

	if ver == "" {
		return fmt.Errorf("no update available")
	}

	// Disconnect if connected and wait for clean shutdown
	a.Disconnect()
	// Give tela a moment to fully exit and release ports
	time.Sleep(1 * time.Second)

	// Update tela CLI
	a.logCommand("Update tela to "+ver, "# downloading tela "+ver)
	a.installTool("tela", ver)

	if isPackageManaged() {
		// Package-managed: CLI updated, GUI must be updated via package manager
		a.mu.Lock()
		a.updatePending = false
		a.mu.Unlock()
		a.logCommand("CLI updated to "+ver, "# GUI update available via your package manager")
		return nil
	}

	// Download telagui update to staging path (next to running binary)
	a.logCommand("Update telagui to "+ver, "# downloading telagui "+ver)
	a.downloadSelfUpdate(ver)

	exe := selfExePath()
	if exe == "" {
		return fmt.Errorf("cannot determine executable path")
	}

	// Apply the update now before relaunching.
	a.applyPendingSelfUpdate()

	// Relaunch from the same path (which is now the new binary)
	cmd := exec.Command(exe)
	cmd.Start()

	os.Exit(0)
	return nil // unreachable
}

// downloadSelfUpdate downloads the telagui binary for the current platform.
func (a *App) downloadSelfUpdate(ver string) {
	ext := ""
	if runtime.GOOS == "windows" {
		ext = ".exe"
	}
	binaryName := fmt.Sprintf("telagui-%s-%s%s", runtime.GOOS, runtime.GOARCH, ext)
	url := fmt.Sprintf("https://github.com/paulmooreparks/tela/releases/download/%s/%s", ver, binaryName)

	os.MkdirAll(telaInstallDir(), 0755)
	updatePath := selfUpdatePath()

	resp, err := http.Get(url)
	if err != nil || resp.StatusCode != 200 {
		if resp != nil {
			resp.Body.Close()
		}
		return
	}
	defer resp.Body.Close()

	tmpPath := updatePath + ".tmp"
	f, err := os.Create(tmpPath)
	if err != nil {
		return
	}
	if _, err := io.Copy(f, resp.Body); err != nil {
		f.Close()
		os.Remove(tmpPath)
		return
	}
	f.Close()

	os.Remove(updatePath)
	os.Rename(tmpPath, updatePath)

	if runtime.GOOS != "windows" {
		os.Chmod(updatePath, 0755)
	}
}

func (a *App) shutdown(ctx context.Context) {
	a.mu.Lock()
	if a.telaProcess != nil {
		killProcessTree(a.telaProcess.Pid)
		a.telaProcess = nil
		a.connected = false
	}
	a.mu.Unlock()

	// Force exit after 2 seconds if goroutines are stuck
	go func() {
		time.Sleep(2 * time.Second)
		os.Exit(0)
	}()
}

