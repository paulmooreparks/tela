// version is set at build time via -ldflags "-X main.version=..."
var version = "dev"

// App is the main application struct exposed to the frontend via Wails bindings.
type App struct {
	ctx context.Context

	mu            sync.Mutex
	cmdLog        []CommandLogEntry
	httpClient    *http.Client
	assignedPorts map[int]bool // ports already assigned in this session

	// Profile-based connection state
	telaProcess *os.Process
	telaOutput  string
	connected   bool

	// Self-update state
	updatePending bool
	updateVersion string
}

// CommandLogEntry records a CLI command generated by the GUI.
type CommandLogEntry struct {
	Time        string `json:"time"`
	Description string `json:"description"`
	Command     string `json:"command"`
}

// KnownHub is a hub the GUI knows about from the local registry.
type KnownHub struct {
	Name     string `json:"name"`
	URL      string `json:"url"`
	HasToken bool   `json:"hasToken"`
	Source   string `json:"source"` // "credentials", "hubs"
}

// HubStatus is the status of a hub and its machines.
type HubStatus struct {
	Online   bool            `json:"online"`
	HubName  string          `json:"hubName"`
	Machines []MachineStatus `json:"machines"`
	Error    string          `json:"error,omitempty"`
}

// MachineStatus is the status of a machine on a hub.
type MachineStatus struct {
	ID             string        `json:"id"`
	Hostname       string        `json:"hostname"`
	OS             string        `json:"os"`
	AgentConnected bool          `json:"agentConnected"`
	SessionCount   int           `json:"sessionCount"`
	LastSeen       string        `json:"lastSeen"`
	Services       []ServiceInfo `json:"services"`
}

// ServiceInfo describes a service on a machine.
type ServiceInfo struct {
	Name  string `json:"name"`
	Port  int    `json:"port"`
	Proto string `json:"proto"`
}

// ProfileConnection represents one connection entry in the profile YAML.
type ProfileConnection struct {
	Hub      string           `yaml:"hub" json:"hub"`
	Machine  string           `yaml:"machine" json:"machine"`
	Token    string           `yaml:"token,omitempty" json:"token,omitempty"`
	Services []ProfileService `yaml:"services" json:"services"`
}

// ProfileService represents one service in a profile connection.
type ProfileService struct {
	Name  string `yaml:"name" json:"name"`
	Local int    `yaml:"local" json:"local"`
}

// Profile is the top-level profile YAML structure.
type Profile struct {
	Connections []ProfileConnection `yaml:"connections" json:"connections"`
}

// ConnectionState is the current connection state returned to the frontend.
type ConnectionState struct {
	Connected   bool               `json:"connected"`
	Pid         int                `json:"pid"`
	Output      string             `json:"output"`
	Connections []ProfileConnection `json:"connections"`
}

// NewApp creates a new App instance.
func NewApp() *App {
	jar, _ := cookiejar.New(nil)
	return &App{
		httpClient: &http.Client{
			Timeout: 15 * time.Second,
			Jar:     jar,
		},
		assignedPorts: make(map[int]bool),
	}
}

func (a *App) startup(ctx context.Context) {
	a.ctx = ctx

	// Apply any pending self-update from a previous run
	a.applyPendingSelfUpdate()

	// Set up system tray icon
	a.setupTray()

	// Check for updates (respects AutoCheckUpdates setting)
	settings := a.GetSettings()
	if settings.AutoCheckUpdates {
		go a.checkForUpdates()
	}
}

// checkForUpdates checks if any managed binary (telagui or tela CLI) is
// behind the latest GitHub release. If so, sets updatePending but does NOT
// auto-download. The user clicks "Restart to Update" to trigger the update.
// Exception: if tela CLI is not installed at all, it is downloaded immediately.
func (a *App) checkForUpdates() {
	latest, err := a.latestRelease()
	if err != nil {
		return // offline
	}

	needsUpdate := false

	// Check telagui version
	if version != latest {
		needsUpdate = true
	}

	// Check tela CLI version
	bin := "tela"
	if runtime.GOOS == "windows" {
		bin += ".exe"
	}
	localPath := filepath.Join(telaInstallDir(), bin)

	if _, err := os.Stat(localPath); err != nil {
		// tela not installed -- download it now (first run)
		a.logCommand("Install tela "+latest, "# first run, downloading tela CLI")
		a.installTool("tela", latest)
	} else {
		// tela exists -- check version
		cmd := exec.Command(localPath, "version")
		hideConsoleWindow(cmd)
		out, _ := cmd.CombinedOutput()
		parts := strings.Fields(strings.TrimSpace(string(out)))
		if len(parts) >= 2 && parts[1] != latest {
			needsUpdate = true
		}
	}

	if needsUpdate {
		a.mu.Lock()
		a.updatePending = true
		a.updateVersion = latest
		a.mu.Unlock()
	}
}

// isPackageManaged returns true if telagui was installed via a package manager
// (deb, rpm, msi, dmg). In that case, self-update is disabled and the user
// should update via their package manager.
func isPackageManaged() bool {
	exe := selfExePath()
	if exe == "" {
		return false
	}
	// Linux: installed to /usr/bin or /usr/local/bin by deb/rpm
	if strings.HasPrefix(exe, "/usr/bin/") || strings.HasPrefix(exe, "/usr/local/bin/") {
		return true
	}
	// macOS: installed to /Applications
	if strings.HasPrefix(exe, "/Applications/") {
		return true
	}
	// Windows: installed to Program Files
	pf := os.Getenv("ProgramFiles")
	pfx86 := os.Getenv("ProgramFiles(x86)")
	if pf != "" && strings.HasPrefix(strings.ToLower(exe), strings.ToLower(pf)) {
		return true
	}
	if pfx86 != "" && strings.HasPrefix(strings.ToLower(exe), strings.ToLower(pfx86)) {
		return true
	}
	return false
}

// selfExePath returns the absolute path of the currently running telagui binary.
func selfExePath() string {
	exe, err := os.Executable()
	if err != nil {
		return ""
	}
	exe, _ = filepath.Abs(exe)
	return exe
}

// selfUpdatePath returns the staging path for a pending update (next to the running binary).
func selfUpdatePath() string {
	exe := selfExePath()
	if exe == "" {
		return ""
	}
	ext := filepath.Ext(exe)
	base := strings.TrimSuffix(exe, ext)
	return base + "-update" + ext
}

// applyPendingSelfUpdate checks for a staged update binary and swaps it in place.
func (a *App) applyPendingSelfUpdate() {
	updatePath := selfUpdatePath()
	if updatePath == "" {
		return
	}
	if _, err := os.Stat(updatePath); err != nil {
		return // no pending update
	}

	currentPath := selfExePath()
	if currentPath == "" {
		return
	}

	if runtime.GOOS == "windows" {
		// Can't overwrite running exe -- rename current to .old first
		oldPath := currentPath + ".old"
		os.Remove(oldPath)
		if err := os.Rename(currentPath, oldPath); err != nil {
			log.Printf("[update] failed to rename current binary: %v", err)
			return
		}
		if err := os.Rename(updatePath, currentPath); err != nil {
			// Rollback
			os.Rename(oldPath, currentPath)
			log.Printf("[update] failed to install update: %v", err)
			return
		}
		// Clean up old binary (may fail if still locked, that's ok)
		go func() {
			time.Sleep(2 * time.Second)
			os.Remove(oldPath)
		}()
	} else {
		// Unix: just rename over the running binary
		if err := os.Rename(updatePath, currentPath); err != nil {
			log.Printf("[update] failed to install update: %v", err)
			return
		}
		os.Chmod(currentPath, 0755)
	}

	log.Printf("[update] applied pending update")
}


