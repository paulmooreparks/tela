// ConnectControlWS connects to the tela control API WebSocket for real-time events.
// Events are forwarded to the frontend via Wails runtime events.
func (a *App) ConnectControlWS() error {
	controlPath := filepath.Join(telaConfigDir(), "run", "control.json")
	data, err := os.ReadFile(controlPath)
	if err != nil {
		return fmt.Errorf("no control file: %w", err)
	}
	var info struct {
		Port  int    `json:"port"`
		Token string `json:"token"`
	}
	if json.Unmarshal(data, &info) != nil || info.Port == 0 {
		return fmt.Errorf("invalid control file")
	}

	wsURL := fmt.Sprintf("ws://127.0.0.1:%d/ws", info.Port)

	conn, _, err := websocket.DefaultDialer.Dial(wsURL, nil)
	if err != nil {
		return fmt.Errorf("ws connect failed: %w", err)
	}

	// Authenticate via first message (avoids token in URL).
	if err := conn.WriteMessage(websocket.TextMessage, []byte(info.Token)); err != nil {
		conn.Close()
		return fmt.Errorf("ws auth failed: %w", err)
	}

	a.mu.Lock()
	// Close any existing connection before replacing.
	if a.controlWS != nil {
		a.controlWS.Close()
	}
	a.controlWS = conn
	a.mu.Unlock()

	// Read events and forward to frontend. When the connection is
	// closed (by DisconnectControlWS or the remote side), ReadMessage
	// returns an error and the goroutine exits. We nil controlWS only
	// if it still points to this connection (avoids clearing a
	// replacement connection opened by a subsequent ConnectControlWS).
	go func() {
		defer func() {
			a.mu.Lock()
			wasAttached := a.attached
			stillOurs := a.controlWS == conn
			if stillOurs {
				a.controlWS = nil
			}
			a.mu.Unlock()

			// If the WebSocket closed unexpectedly while we were attached,
			// the external tela process likely died. Reset to disconnected.
			if stillOurs && wasAttached {
				a.mu.Lock()
				a.connected = false
				a.attached = false
				a.mu.Unlock()
				if a.ctx != nil {
					wailsRuntime.EventsEmit(a.ctx, "tela:disconnected", true)
				}
				log.Printf("[telavisor] control WebSocket closed -- external tela may have exited")
			}
		}()

		for {
			_, message, err := conn.ReadMessage()
			if err != nil {
				return
			}
			wailsRuntime.EventsEmit(a.ctx, "tela:event", string(message))
		}
	}()

	return nil
}

// DisconnectControlWS closes the WebSocket connection.
func (a *App) DisconnectControlWS() {
	a.mu.Lock()
	defer a.mu.Unlock()
	if a.controlWS != nil {
		a.controlWS.Close()
		a.controlWS = nil
	}
}

// SendControlCommand sends a command to tela via the WebSocket.
