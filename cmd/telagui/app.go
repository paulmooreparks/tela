// GetKnownHubs returns the hubs visible through the active portal
// source. The portal owns the directory; TelaVisor no longer reads
// hubs.yaml or credentials.yaml directly. The HasToken field is
// always true here because the portal stores the admin token
// internally and never exposes it to the frontend; admin operations
// either succeed or surface a "no admin token" error from the proxy.
// Source is the portal source name (e.g. "Local") so the frontend
// can group hubs by source if it wants to.
func (a *App) GetKnownHubs() []KnownHub {
	clients, err := a.enabledPortalClients()
	if err != nil {
		return nil
	}
	ctx, cancel := context.WithTimeout(context.Background(), 5*time.Second)
	defer cancel()

	result, err := portalaggregate.Merge(ctx, clients)
	if err != nil {
		return nil
	}
	out := make([]KnownHub, 0, len(result.Hubs))
	for _, h := range result.Hubs {
		out = append(out, KnownHub{
			Name:     h.Name,
			URL:      h.URL,
			HasToken: true,
			Source:   h.PreferredSource,
		})
	}
	return out
}

func hubNameFromURL(url string) string {
	url = strings.TrimPrefix(url, "wss://")
	url = strings.TrimPrefix(url, "ws://")
	url = strings.TrimPrefix(url, "https://")
	url = strings.TrimPrefix(url, "http://")
	url = strings.TrimSuffix(url, "/")
	return url
}

// GetHubStatus queries a hub for its machine/service status. The hub
// is identified by name; the URL and stored token are resolved via
// the active portal source through fetchHubPublicEndpoint. The body
// of the response is decoded into the HubStatus shape the frontend
// consumes.
func (a *App) GetHubStatus(hubName string) HubStatus {
	respBody, err := a.fetchHubPublicEndpoint(hubName, "/api/status")
	if err != nil {
		return HubStatus{Error: err.Error()}
	}

	var raw struct {
		HubName  string `json:"hubName"`
		Machines []struct {
			ID             string `json:"id"`
			Hostname       string `json:"hostname"`
			OS             string `json:"os"`
			AgentConnected bool   `json:"agentConnected"`
			SessionCount   int    `json:"sessionCount"`
			LastSeen       string `json:"lastSeen"`
			Services       []struct {
				Name  string `json:"name"`
				Port  int    `json:"port"`
				Proto string `json:"proto"`
			} `json:"services"`
			Capabilities map[string]interface{} `json:"capabilities"`
		} `json:"machines"`
	}
	if err := json.Unmarshal(respBody, &raw); err != nil {
		return HubStatus{Error: err.Error()}
	}

	status := HubStatus{
		Online:  true,
		HubName: raw.HubName,
	}
	for _, m := range raw.Machines {
		ms := MachineStatus{
			ID:             m.ID,
			Hostname:       m.Hostname,
			OS:             m.OS,
			AgentConnected: m.AgentConnected,
			SessionCount:   m.SessionCount,
			LastSeen:       m.LastSeen,
			Capabilities:   m.Capabilities,
		}
		for _, s := range m.Services {
			ms.Services = append(ms.Services, ServiceInfo{
				Name:  s.Name,
				Port:  s.Port,
				Proto: s.Proto,
			})
		}
		status.Machines = append(status.Machines, ms)
	}
	return status
}

