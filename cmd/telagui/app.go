// App is the main application struct exposed to the frontend via Wails bindings.
type App struct {
	ctx context.Context

	mu         sync.Mutex
	cmdLog     []CommandLogEntry
	httpClient *http.Client

	// Profile-based connection state
	telaProcess *os.Process
	telaOutput  string
	connected   bool
}

// CommandLogEntry records a CLI command generated by the GUI.
type CommandLogEntry struct {
	Time        string `json:"time"`
	Description string `json:"description"`
	Command     string `json:"command"`
}

// KnownHub is a hub the GUI knows about from the local registry.
type KnownHub struct {
	Name     string `json:"name"`
	URL      string `json:"url"`
	HasToken bool   `json:"hasToken"`
	Source   string `json:"source"` // "credentials", "hubs"
}

// HubStatus is the status of a hub and its machines.
type HubStatus struct {
	Online   bool            `json:"online"`
	HubName  string          `json:"hubName"`
	Machines []MachineStatus `json:"machines"`
	Error    string          `json:"error,omitempty"`
}

// MachineStatus is the status of a machine on a hub.
type MachineStatus struct {
	ID             string        `json:"id"`
	Hostname       string        `json:"hostname"`
	OS             string        `json:"os"`
	AgentConnected bool          `json:"agentConnected"`
	SessionCount   int           `json:"sessionCount"`
	LastSeen       string        `json:"lastSeen"`
	Services       []ServiceInfo `json:"services"`
}

// ServiceInfo describes a service on a machine.
type ServiceInfo struct {
	Name  string `json:"name"`
	Port  int    `json:"port"`
	Proto string `json:"proto"`
}

// ProfileConnection represents one connection entry in the profile YAML.
type ProfileConnection struct {
	Hub      string           `yaml:"hub" json:"hub"`
	Machine  string           `yaml:"machine" json:"machine"`
	Token    string           `yaml:"token,omitempty" json:"token,omitempty"`
	Services []ProfileService `yaml:"services" json:"services"`
}

// ProfileService represents one service in a profile connection.
type ProfileService struct {
	Name  string `yaml:"name" json:"name"`
	Local int    `yaml:"local" json:"local"`
}

// Profile is the top-level profile YAML structure.
type Profile struct {
	Connections []ProfileConnection `yaml:"connections" json:"connections"`
}

// ConnectionState is the current connection state returned to the frontend.
type ConnectionState struct {
	Connected   bool               `json:"connected"`
	Pid         int                `json:"pid"`
	Output      string             `json:"output"`
	Connections []ProfileConnection `json:"connections"`
}

// NewApp creates a new App instance.
func NewApp() *App {
	jar, _ := cookiejar.New(nil)
	return &App{
		httpClient: &http.Client{
			Timeout: 15 * time.Second,
			Jar:     jar,
		},
	}
}

func (a *App) startup(ctx context.Context) {
	a.ctx = ctx
}

func (a *App) shutdown(ctx context.Context) {
	a.mu.Lock()
	if a.telaProcess != nil {
		a.telaProcess.Kill()
		a.telaProcess = nil
		a.connected = false
	}
	a.mu.Unlock()
}

// --- Command Log ---

// GetCommandLog returns all command log entries.
func (a *App) GetCommandLog() []CommandLogEntry {
	a.mu.Lock()
	defer a.mu.Unlock()
	result := make([]CommandLogEntry, len(a.cmdLog))
	copy(result, a.cmdLog)
	return result
}

func (a *App) logCommand(description, command string) {
	a.mu.Lock()
	defer a.mu.Unlock()
	a.cmdLog = append(a.cmdLog, CommandLogEntry{
		Time:        time.Now().Format("2006-01-02 15:04:05"),
		Description: description,
		Command:     command,
	})
}

// --- Local Registry ---

func telaConfigDir() string {
	if runtime.GOOS == "windows" {
		if appData := os.Getenv("APPDATA"); appData != "" {
			return filepath.Join(appData, "tela")
		}
	}
	home, _ := os.UserHomeDir()
	return filepath.Join(home, ".tela")
}

func profilesDir() string {
	return filepath.Join(telaConfigDir(), "profiles")
}

func profilePath() string {
	return filepath.Join(profilesDir(), "telagui.yaml")
}

// GetKnownHubs returns all hubs the user has configured locally.
// Reads from credentials.yaml and hubs.yaml.
func (a *App) GetKnownHubs() []KnownHub {
	seen := make(map[string]bool)
	var hubs []KnownHub

	// 1. Credential store -- hubs with stored tokens
	store, err := credstore.Load(credstore.UserPath())
	if err == nil && store != nil {
		for url, cred := range store.Hubs {
			name := hubNameFromURL(url)
			hubs = append(hubs, KnownHub{
				Name:     name,
				URL:      url,
				HasToken: cred.Token != "",
				Source:   "credentials",
			})
			seen[url] = true
		}
	}

	// 2. Hub aliases -- short names mapped to URLs
	hubsFile := filepath.Join(telaConfigDir(), "hubs.yaml")
	if data, err := os.ReadFile(hubsFile); err == nil {
		var cfg struct {
			Hubs map[string]string `yaml:"hubs"`
		}
		if yaml.Unmarshal(data, &cfg) == nil {
			for name, url := range cfg.Hubs {
				if !seen[url] {
					hasToken := false
					if store != nil {
						_, hasToken = store.Hubs[url]
					}
					hubs = append(hubs, KnownHub{
						Name:     name,
						URL:      url,
						HasToken: hasToken,
						Source:   "hubs",
					})
					seen[url] = true
				}
			}
		}
	}

	return hubs
}

func hubNameFromURL(url string) string {
	url = strings.TrimPrefix(url, "wss://")
	url = strings.TrimPrefix(url, "ws://")
	url = strings.TrimPrefix(url, "https://")
	url = strings.TrimPrefix(url, "http://")
	url = strings.TrimSuffix(url, "/")
	return url
}

// GetHubStatus queries a hub for its machine/service status.
func (a *App) GetHubStatus(hubURL string) HubStatus {
	token := credstore.LookupToken(hubURL)
	if token == "" {
		return HubStatus{Error: "no token stored for this hub"}
	}

	// Convert wss:// to https:// for the API call
	apiURL := hubURL
	apiURL = strings.Replace(apiURL, "wss://", "https://", 1)
	apiURL = strings.Replace(apiURL, "ws://", "http://", 1)
	apiURL = strings.TrimSuffix(apiURL, "/") + "/api/status"

	req, err := http.NewRequest("GET", apiURL, nil)
	if err != nil {
		return HubStatus{Error: err.Error()}
	}
	req.Header.Set("Authorization", "Bearer "+token)

	resp, err := a.httpClient.Do(req)
	if err != nil {
		return HubStatus{Error: err.Error()}
	}
	defer resp.Body.Close()

	if resp.StatusCode != 200 {
		return HubStatus{Error: fmt.Sprintf("HTTP %d", resp.StatusCode)}
	}

	var raw struct {
		HubName  string `json:"hubName"`
		Machines []struct {
			ID             string `json:"id"`
			Hostname       string `json:"hostname"`
			OS             string `json:"os"`
			AgentConnected bool   `json:"agentConnected"`
			SessionCount   int    `json:"sessionCount"`
			LastSeen       string `json:"lastSeen"`
			Services       []struct {
				Name  string `json:"name"`
				Port  int    `json:"port"`
				Proto string `json:"proto"`
			} `json:"services"`
		} `json:"machines"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&raw); err != nil {
		return HubStatus{Error: err.Error()}
	}

	status := HubStatus{
		Online:  true,
		HubName: raw.HubName,
	}
	for _, m := range raw.Machines {
		ms := MachineStatus{
			ID:             m.ID,
			Hostname:       m.Hostname,
			OS:             m.OS,
			AgentConnected: m.AgentConnected,
			SessionCount:   m.SessionCount,
			LastSeen:       m.LastSeen,
		}
		for _, s := range m.Services {
			ms.Services = append(ms.Services, ServiceInfo{
				Name:  s.Name,
				Port:  s.Port,
				Proto: s.Proto,
			})
		}
		status.Machines = append(status.Machines, ms)
	}
	return status
}

// --- Hub Management ---

// AddHub stores a hub URL and token in the credential store.
func (a *App) AddHub(url, token string) error {
	wsURL := toWSURL(url)
	a.logCommand("Add hub "+wsURL, "tela login "+wsURL)

	store, err := credstore.Load(credstore.UserPath())
	if err != nil {
		store = &credstore.Store{Hubs: make(map[string]credstore.Credential)}
	}
	store.Set(wsURL, credstore.Credential{Token: token})
	return store.Save(credstore.UserPath())
}

// RemoveHub removes a hub from the credential store.
func (a *App) RemoveHub(url string) error {
	wsURL := toWSURL(url)
	a.logCommand("Remove hub "+wsURL, "tela logout "+wsURL)

	// Remove from credential store
	store, err := credstore.Load(credstore.UserPath())
	if err == nil {
		store.Remove(wsURL)
		store.Save(credstore.UserPath())
	}

	// Remove from hubs.yaml aliases
	hubsFile := filepath.Join(telaConfigDir(), "hubs.yaml")
	if data, err := os.ReadFile(hubsFile); err == nil {
		var cfg struct {
			Hubs map[string]string `yaml:"hubs"`
		}
		if yaml.Unmarshal(data, &cfg) == nil && cfg.Hubs != nil {
			changed := false
			for name, hubURL := range cfg.Hubs {
				if hubURL == url || hubURL == wsURL {
					delete(cfg.Hubs, name)
					changed = true
				}
			}
			if changed {
				out, _ := yaml.Marshal(&cfg)
				os.WriteFile(hubsFile, out, 0600)
			}
		}
	}

	return nil
}

// GetStoredToken checks the credential store for a token for the given hub URL.
func (a *App) GetStoredToken(hubURL string) string {
	return credstore.LookupToken(toWSURL(hubURL))
}

// --- Docker Integration ---

// DockerGetToken runs telahubd user show-owner inside a Docker container.
func (a *App) DockerGetToken(container, role string) (string, error) {
	if role == "" {
		role = "show-owner"
	}
	cmd := fmt.Sprintf("docker exec %s telahubd user %s -config /app/data/telahubd.yaml",
		container, role)
	a.logCommand("Get "+role+" token from container "+container, cmd)

	out, err := exec.Command("docker", "exec", container,
		"telahubd", "user", role, "-config", "/app/data/telahubd.yaml").CombinedOutput()
	if err != nil {
		return "", fmt.Errorf("%s: %s", err, strings.TrimSpace(string(out)))
	}
	return strings.TrimSpace(string(out)), nil
}

// DockerListContainers lists running Docker containers (for hub discovery).
func (a *App) DockerListContainers() ([]string, error) {
	out, err := exec.Command("docker", "ps", "--format", "{{.Names}}").CombinedOutput()
	if err != nil {
		return nil, fmt.Errorf("docker not available: %s", strings.TrimSpace(string(out)))
	}
	lines := strings.Split(strings.TrimSpace(string(out)), "\n")
	var names []string
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line != "" {
			names = append(names, line)
		}
	}
	return names, nil
}

// --- Profile-Based Connection ---

// AssignLocalPort picks a local port for a service. Uses the service's
// advertised port if available, otherwise tries port+10000.
func (a *App) AssignLocalPort(servicePort int) int {
	if isPortAvailable(servicePort) {
		return servicePort
	}
	alt := servicePort + 10000
	if isPortAvailable(alt) {
		return alt
	}
	// Fall back to letting the OS assign one
	return servicePort
}

func isPortAvailable(port int) bool {
	ln, err := net.Listen("tcp", fmt.Sprintf("127.0.0.1:%d", port))
	if err != nil {
		return false
	}
	ln.Close()
	return true
}

// SaveProfile writes the profile YAML and returns its path.
func (a *App) SaveProfile(connectionsJSON string) (string, error) {
	var connections []ProfileConnection
	if err := json.Unmarshal([]byte(connectionsJSON), &connections); err != nil {
		return "", fmt.Errorf("invalid connections: %w", err)
	}

	// Resolve tokens from credential store for each connection.
	// Tokens are written directly into the profile (file is 0600).
	for i := range connections {
		if connections[i].Token == "" || connections[i].Token == "${TELA_TOKEN}" {
			token := credstore.LookupToken(connections[i].Hub)
			if token != "" {
				connections[i].Token = token
			}
		}
	}

	profile := Profile{Connections: connections}

	data, err := yaml.Marshal(profile)
	if err != nil {
		return "", fmt.Errorf("marshal profile: %w", err)
	}

	dir := profilesDir()
	if err := os.MkdirAll(dir, 0700); err != nil {
		return "", fmt.Errorf("create profiles dir: %w", err)
	}

	path := profilePath()
	if err := os.WriteFile(path, data, 0600); err != nil {
		return "", fmt.Errorf("write profile: %w", err)
	}

	a.logCommand("Save profile", "cat "+path)
	return path, nil
}

// Connect starts tela connect -profile telagui in the background.
func (a *App) Connect(connectionsJSON string) (string, error) {
	a.mu.Lock()
	if a.connected {
		a.mu.Unlock()
		return "", fmt.Errorf("already connected -- disconnect first")
	}
	a.mu.Unlock()

	telaPath := a.findTool("tela")
	if telaPath == "" {
		return "", fmt.Errorf("tela not found -- install it or add it to PATH")
	}

	path, err := a.SaveProfile(connectionsJSON)
	if err != nil {
		return "", err
	}

	cmdStr := "tela connect -profile " + path
	a.logCommand("Connect using profile", cmdStr)

	cmd := exec.Command(telaPath, "connect", "-profile", path)
	hideConsoleWindow(cmd)

	outPipe, _ := cmd.StdoutPipe()
	cmd.Stderr = cmd.Stdout

	if err := cmd.Start(); err != nil {
		return "", fmt.Errorf("failed to start tela: %w", err)
	}

	pid := cmd.Process.Pid

	a.mu.Lock()
	a.telaProcess = cmd.Process
	a.connected = true
	a.telaOutput = ""
	a.mu.Unlock()

	go func() {
		buf := make([]byte, 4096)
		for {
			n, err := outPipe.Read(buf)
			if n > 0 {
				a.mu.Lock()
				a.telaOutput += string(buf[:n])
				a.mu.Unlock()
			}
			if err != nil {
				break
			}
		}
		cmd.Wait()
		a.mu.Lock()
		a.connected = false
		a.mu.Unlock()
	}()

	return fmt.Sprintf("Connected (pid %d)", pid), nil
}

// Disconnect kills the running tela process.
func (a *App) Disconnect() error {
	a.mu.Lock()
	defer a.mu.Unlock()
	if a.telaProcess != nil {
		a.telaProcess.Kill()
		a.telaProcess = nil
		a.connected = false
		a.telaOutput = ""
		a.logCommand("Disconnect", "kill <tela-pid>")
		return nil
	}
	return fmt.Errorf("not connected")
}

// GetConnectionState returns the current connection state.
func (a *App) GetConnectionState() ConnectionState {
	a.mu.Lock()
	defer a.mu.Unlock()

	state := ConnectionState{
		Connected: a.connected,
		Output:    a.telaOutput,
	}

	if a.telaProcess != nil {
		state.Pid = a.telaProcess.Pid
	}

	// Load the saved profile to show connection details
	if a.connected {
		data, err := os.ReadFile(profilePath())
		if err == nil {
			var profile Profile
			if yaml.Unmarshal(data, &profile) == nil {
				state.Connections = profile.Connections
			}
		}
	}

	return state
}

// --- Tool Management ---

func (a *App) findTool(name string) string {
	bin := name
	if runtime.GOOS == "windows" {
		bin += ".exe"
	}
	if p, err := exec.LookPath(bin); err == nil {
		return p
	}
	local := filepath.Join(telaInstallDir(), bin)
	if _, err := os.Stat(local); err == nil {
		return local
	}
	return ""
}

func telaInstallDir() string {
	if runtime.GOOS == "windows" {
		return filepath.Join(os.Getenv("LOCALAPPDATA"), "Tela", "bin")
	}
	home, _ := os.UserHomeDir()
	return filepath.Join(home, ".tela", "bin")
}

// EnsureTool checks if a tool is installed, downloads it if not.
func (a *App) EnsureTool(name string) (string, error) {
	path := a.findTool(name)
	if path != "" {
		return path, nil
	}

	version, err := a.latestRelease()
	if err != nil {
		return "", fmt.Errorf("cannot determine latest version: %w", err)
	}

	return a.installTool(name, version)
}

func (a *App) latestRelease() (string, error) {
	req, _ := http.NewRequest("GET", "https://api.github.com/repos/paulmooreparks/tela/releases/latest", nil)
	req.Header.Set("Accept", "application/vnd.github.v3+json")
	resp, err := a.httpClient.Do(req)
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()
	var release struct {
		TagName string `json:"tag_name"`
	}
	json.NewDecoder(resp.Body).Decode(&release)
	return release.TagName, nil
}

func (a *App) installTool(name, version string) (string, error) {
	ext := ""
	if runtime.GOOS == "windows" {
		ext = ".exe"
	}
	url := fmt.Sprintf("https://github.com/paulmooreparks/tela/releases/download/%s/%s-%s-%s%s",
		version, name, runtime.GOOS, runtime.GOARCH, ext)

	installDir := telaInstallDir()
	os.MkdirAll(installDir, 0755)
	destPath := filepath.Join(installDir, name+ext)

	a.logCommand("Install "+name+" "+version,
		fmt.Sprintf("curl -fSL -o %q %s", destPath, url))

	resp, err := http.Get(url)
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		return "", fmt.Errorf("download failed: HTTP %d", resp.StatusCode)
	}

	f, err := os.Create(destPath)
	if err != nil {
		return "", err
	}
	io.Copy(f, resp.Body)
	f.Close()

	if runtime.GOOS != "windows" {
		os.Chmod(destPath, 0755)
	}
	return destPath, nil
}

// --- Helpers ---

func toWSURL(hubURL string) string {
	if strings.HasPrefix(hubURL, "https://") {
		return "wss://" + strings.TrimPrefix(hubURL, "https://")
	}
	if strings.HasPrefix(hubURL, "http://") {
		return "ws://" + strings.TrimPrefix(hubURL, "http://")
	}
	if !strings.HasPrefix(hubURL, "wss://") && !strings.HasPrefix(hubURL, "ws://") {
		return "wss://" + hubURL
	}
	return hubURL
}
