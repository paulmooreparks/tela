// AssignLocalPort picks a unique local port for a service. Checks system
// availability AND avoids ports already assigned to other selected services.
func (a *App) AssignLocalPort(servicePort int) int {
	a.mu.Lock()
	defer a.mu.Unlock()

	// Try the advertised port first
	if !a.assignedPorts[servicePort] && isPortAvailable(servicePort) {
		a.assignedPorts[servicePort] = true
		return servicePort
	}

	// Try port + 10000, then increment until we find one
	candidate := servicePort + 10000
	for i := 0; i < 100; i++ {
		if !a.assignedPorts[candidate] && isPortAvailable(candidate) {
			a.assignedPorts[candidate] = true
			return candidate
		}
		candidate++
	}

	// Last resort
	a.assignedPorts[servicePort] = true
	return servicePort
}

// PortRequest is a service that needs a local port assigned.
type PortRequest struct {
	Key         string `json:"key"`         // unique key like "hub||machine||service"
	ServicePort int    `json:"servicePort"` // the advertised remote port
}

// PortAssignment is the result of port resolution.
type PortAssignment struct {
	Key       string `json:"key"`
	LocalPort int    `json:"localPort"`
}

// ResolveAllPorts takes all selected services and assigns clash-free local ports.
// Called whenever selections change. Resets all previous assignments.
func (a *App) ResolveAllPorts(requestsJSON string) ([]PortAssignment, error) {
	var requests []PortRequest
	if err := json.Unmarshal([]byte(requestsJSON), &requests); err != nil {
		return nil, fmt.Errorf("invalid requests: %w", err)
	}

	a.mu.Lock()
	defer a.mu.Unlock()

	// Reset all assignments
	a.assignedPorts = make(map[int]bool)

	var results []PortAssignment
	for _, req := range requests {
		port := req.ServicePort

		// Try the advertised port
		if !a.assignedPorts[port] && isPortAvailable(port) {
			a.assignedPorts[port] = true
			results = append(results, PortAssignment{Key: req.Key, LocalPort: port})
			continue
		}

		// Try port + 10000, then increment
		candidate := port + 10000
		found := false
		for i := 0; i < 100; i++ {
			if !a.assignedPorts[candidate] && isPortAvailable(candidate) {
				a.assignedPorts[candidate] = true
				results = append(results, PortAssignment{Key: req.Key, LocalPort: candidate})
				found = true
				break
			}
			candidate++
		}
		if !found {
			// Last resort -- use the original port
			a.assignedPorts[port] = true
			results = append(results, PortAssignment{Key: req.Key, LocalPort: port})
		}
	}

	return results, nil
}

// ReleaseLocalPort frees a previously assigned port.
func (a *App) ReleaseLocalPort(port int) {
	a.mu.Lock()
	defer a.mu.Unlock()
	delete(a.assignedPorts, port)
}

func isPortAvailable(port int) bool {
	// Check both loopback and all interfaces (Docker binds on 0.0.0.0)
	for _, addr := range []string{"127.0.0.1", "0.0.0.0"} {
		ln, err := net.Listen("tcp", fmt.Sprintf("%s:%d", addr, port))
		if err != nil {
			return false
		}
		ln.Close()
	}
	return true
}

// LoopbackAddr computes a deterministic loopback address for a
// hub+machine pair using the same algorithm as the tela client.
// Exposed as a Wails binding so the frontend can display the
// expected bind address before connecting.
func (a *App) LoopbackAddr(hubURL, machine string) string {
	return loopbackAddr("127.88", hubURL, machine)
}

func loopbackAddr(prefix, hubURL, machine string) string {
	h := sha256.Sum256([]byte(hubURL + "/" + machine))
	o3 := (uint16(h[0])<<8|uint16(h[1]))%255 + 1
	o4 := (uint16(h[2])<<8|uint16(h[3]))%255 + 1
	return fmt.Sprintf("%s.%d.%d", prefix, o3, o4)
}

// newProfileUUID returns a v4 UUID for use as Profile.ID. Uses
// crypto/rand so the result is suitable as a stable identity.
func newProfileUUID() string {
	var b [16]byte
	if _, err := rand.Read(b[:]); err != nil {
		// crypto/rand failure is exceptional; fall back to a
		// timestamp-derived value so the caller still gets a
		// non-empty string.
		return fmt.Sprintf("0-0-0-0-%x", time.Now().UnixNano())
	}
	b[6] = (b[6] & 0x0f) | 0x40 // version 4
	b[8] = (b[8] & 0x3f) | 0x80 // variant bits
	s := hex.EncodeToString(b[:])
	return s[0:8] + "-" + s[8:12] + "-" + s[12:16] + "-" + s[16:20] + "-" + s[20:32]
}

// loadProfileFile reads a profile YAML and applies the lazy 5e
// migration: if Profile.ID is empty, generate one. Returns the
// (possibly mutated) profile, the path it was loaded from, and a
// dirty flag. Callers that observe dirty=true should rewrite the
// file at the next opportunity.
func loadProfileFile(path string) (Profile, bool, error) {
	var profile Profile
	data, err := os.ReadFile(path)
	if err != nil {
		return profile, false, err
	}
	if err := yaml.Unmarshal(data, &profile); err != nil {
		return profile, false, err
	}

	dirty := false
	if profile.ID == "" {
		profile.ID = newProfileUUID()
		dirty = true
	}
	expectedName := strings.TrimSuffix(filepath.Base(path), filepath.Ext(path))
	if profile.Name != expectedName {
		profile.Name = expectedName
		dirty = true
	}
	return profile, dirty, nil
}

// writeProfileFile marshals a profile and writes it atomically to
// path. The on-disk permissions are 0600.
func writeProfileFile(path string, profile Profile) error {
	data, err := yaml.Marshal(&profile)
	if err != nil {
		return fmt.Errorf("marshal profile: %w", err)
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o700); err != nil {
		return fmt.Errorf("create profiles dir: %w", err)
	}
	return os.WriteFile(path, data, 0o600)
}

// LoadProfile reads the saved profile and returns its connections.
// Pre-5e profiles are migrated in place: a UUID is generated, hub
// labels are resolved against the merged hub view to populate HubID,
// and the file is rewritten so subsequent loads skip migration.
func (a *App) LoadProfile() []ProfileConnection {
	path := profilePath()
	profile, dirty, err := loadProfileFile(path)
	if err != nil {
		return nil
	}
	// Lazy 5e identity: populate HubID for any connection that
	// lacks one but whose Hub label matches a hub in the merged view.
	if a.fillConnectionHubIDs(profile.Connections) {
		dirty = true
	}
	if dirty {
		_ = writeProfileFile(path, profile)
	}
	return profile.Connections
}

// fillConnectionHubIDs runs the merged hub view from
// portalaggregate.Merge and populates HubID for any connection that
// is missing one but whose Hub label matches a known hub. Returns
// true when any connection was modified. Errors are non-fatal: a
// failed lookup just leaves HubID empty for the next attempt.
func (a *App) fillConnectionHubIDs(conns []ProfileConnection) bool {
	needsFill := false
	for _, c := range conns {
		if c.HubID == "" {
			needsFill = true
			break
		}
	}
	if !needsFill {
		return false
	}

	clients, err := a.enabledPortalClients()
	if err != nil || len(clients) == 0 {
		return false
	}
	ctx, cancel := context.WithTimeout(context.Background(), 5*time.Second)
	defer cancel()
	merged, err := portalaggregate.Merge(ctx, clients)
	if err != nil {
		return false
	}

	// Build a name → hubId index. Skip empty hubIds (pre-1.1 hubs).
	byName := make(map[string]string, len(merged.Hubs))
	for _, h := range merged.Hubs {
		if h.HubID != "" {
			byName[h.Name] = h.HubID
		}
	}

	changed := false
	for i, c := range conns {
		if c.HubID != "" || c.Hub == "" {
			continue
		}
		// Match by display name; URL-form Hub fields are left for
		// the user to convert via the editor.
		if id, ok := byName[c.Hub]; ok {
			conns[i].HubID = id
			changed = true
		}
	}
	return changed
}

// SaveProfile writes the profile YAML and returns its path.
func (a *App) SaveProfile(connectionsJSON string) (string, error) {
	var connections []ProfileConnection
	if err := json.Unmarshal([]byte(connectionsJSON), &connections); err != nil {
		return "", fmt.Errorf("invalid connections: %w", err)
	}

	// Do not embed tokens in the profile. tela connect -profile
	// resolves tokens from the credential store at connect time.
	// This avoids duplicating credentials across files.
	for i := range connections {
		connections[i].Token = ""
	}

	// Load the existing profile (if any) to preserve MTU, mount,
	// and the stable ID. loadProfileFile generates an ID for
	// pre-5e files; we want to keep it.
	path := profilePath()
	existing, _, _ := loadProfileFile(path)
	if existing.ID == "" {
		existing.ID = newProfileUUID()
	}
	if existing.Name == "" {
		existing.Name = strings.TrimSuffix(filepath.Base(path), filepath.Ext(path))
	}

	profile := Profile{
		ID:          existing.ID,
		Name:        existing.Name,
		Connections: connections,
		MTU:         existing.MTU,
		Mount:       existing.Mount,
	}

	if err := writeProfileFile(path, profile); err != nil {
		return "", fmt.Errorf("write profile: %w", err)
	}
	return path, nil
}

// Connect starts tela connect -profile telavisor in the background.
// TryAttach is called by the frontend after event listeners are registered.
// It checks for a running tela and emits tela:attached if found.
func (a *App) TryAttach() bool {
	if a.AttachToExisting() {
		if a.ctx != nil {
			wailsRuntime.EventsEmit(a.ctx, "tela:attached", true)
		}
		return true
	}
	return false
}

// AttachToExisting checks if tela is already running (service, terminal, etc.)
// and attaches to it without launching a new process. Returns true if attached.
func (a *App) AttachToExisting() bool {
	a.mu.Lock()
	if a.connected {
		a.mu.Unlock()
		return true
	}
	a.mu.Unlock()

	base, token, err := controlEndpoint()
	if err != nil {
		return false
	}

	// Probe the control API
	req, _ := http.NewRequest("GET", base+"/services", nil)
	req.Header.Set("Authorization", "Bearer "+token)
	resp, err := a.doRequest(req, 3*time.Second)
	if err != nil {
		return false
	}
	resp.Body.Close()
	if resp.StatusCode != 200 {
		return false
	}

	// tela is running externally -- attach
	a.mu.Lock()
	a.connected = true
	a.attached = true
	a.telaProcess = nil
	a.mu.Unlock()

	log.Printf("[telavisor] attached to existing tela at %s", base)
	return true
}

// Detach disconnects TV from an externally-running tela without stopping it.
func (a *App) Detach() {
	a.mu.Lock()
	a.connected = false
	a.attached = false
	a.telaProcess = nil
	a.mu.Unlock()
	a.DisconnectControlWS()
	a.logCommand("Detached", "Disconnected from external tela (process still running)")
}

func (a *App) Connect(connectionsJSON string) (string, error) {
	a.mu.Lock()
	if a.connected {
		a.mu.Unlock()
		return "", fmt.Errorf("already connected -- disconnect first")
	}
	a.mu.Unlock()

	// Check if tela is already running externally
	if a.AttachToExisting() {
		if a.ctx != nil {
			wailsRuntime.EventsEmit(a.ctx, "tela:attached", true)
		}
		return "Attached to running tela", nil
	}

	telaPath := a.findTool("tela")
	if telaPath == "" {
		// Auto-download from GitHub
		var err error
		telaPath, err = a.EnsureTool("tela")
		if err != nil {
			return "", fmt.Errorf("tela not available and download failed: %w", err)
		}
	}

	path, err := a.SaveProfile(connectionsJSON)
	if err != nil {
		return "", err
	}

	// Sync hub tokens from the portal store to the local credential
	// store so tela connect can authenticate. The portal owns the
	// admin tokens; the tela CLI reads from ~/.tela/credentials.yaml.
	a.syncHubTokensToCredstore(connectionsJSON)

	cmdStr := "tela connect -profile " + path
	a.logCommand("Connect using profile", cmdStr)

	cmd := exec.Command(telaPath, "connect", "-profile", path)
	hideConsoleWindow(cmd)

	outPipe, _ := cmd.StdoutPipe()
	cmd.Stderr = cmd.Stdout

	if err := cmd.Start(); err != nil {
		return "", fmt.Errorf("failed to start tela: %w", err)
	}

	pid := cmd.Process.Pid

	a.mu.Lock()
	a.telaProcess = cmd.Process
	a.connected = true
	a.telaOutput = ""
	a.mu.Unlock()

	go func() {
		buf := make([]byte, 4096)
		for {
			n, err := outPipe.Read(buf)
			if n > 0 {
				chunk := string(buf[:n])
				a.mu.Lock()
				a.telaOutput += chunk
				a.mu.Unlock()
				// Push to frontend in real time
				if a.ctx != nil {
					wailsRuntime.EventsEmit(a.ctx, "tela:output", chunk)
				}
			}
			if err != nil {
				break
			}
		}
		cmd.Wait()
		a.mu.Lock()
		a.connected = false
		a.telaProcess = nil
		a.mu.Unlock()
		// Notify frontend immediately so the UI updates without
		// waiting for the next connection poll.
		if a.ctx != nil {
			wailsRuntime.EventsEmit(a.ctx, "tela:exited")
		}
	}()

	return fmt.Sprintf("Connected (pid %d)", pid), nil
}

// Disconnect stops the tela connection. If attached to an external process,
// it detaches without killing. If TV launched the process, it shuts it down.
func (a *App) Disconnect() error {
	a.mu.Lock()
	isAttached := a.attached
	a.mu.Unlock()

	// Attached to external tela: just detach, don't kill
	if isAttached {
		a.Detach()
		return nil
	}

	// TV launched this process: shut it down
	if a.disconnectViaControlAPI() {
		a.mu.Lock()
		a.telaProcess = nil
		a.connected = false
		a.mu.Unlock()
		return nil
	}

	// Fallback: kill the process directly
	a.mu.Lock()
	defer a.mu.Unlock()
	if a.telaProcess != nil {
		pid := a.telaProcess.Pid
		killProcessTree(pid)
		a.telaProcess.Kill()
		a.telaProcess = nil
		a.connected = false
		a.logCommand("Disconnect (forced)", fmt.Sprintf("taskkill /PID %d /T /F", pid))
		return nil
	}
	a.connected = false
	return nil
}

// disconnectViaControlAPI reads the control file and sends DELETE / to the tela control API.
func (a *App) disconnectViaControlAPI() bool {
	controlPath := filepath.Join(telaConfigDir(), "run", "control.json")
	data, err := os.ReadFile(controlPath)
	if err != nil {
		return false
	}

	var info struct {
		Port  int    `json:"port"`
		Token string `json:"token"`
	}
	if json.Unmarshal(data, &info) != nil || info.Port == 0 || info.Token == "" {
		return false
	}

	url := fmt.Sprintf("http://127.0.0.1:%d/", info.Port)
	req, err := http.NewRequest("DELETE", url, nil)
	if err != nil {
		return false
	}
	req.Header.Set("Authorization", "Bearer "+info.Token)

	resp, err := a.doRequestLogged(req, 5*time.Second)
	if err != nil {
		return false
	}
	defer resp.Body.Close()

	// Wait for the process to exit
	a.mu.Lock()
	proc := a.telaProcess
	a.mu.Unlock()
	if proc != nil {
		// Give it up to 5 seconds to exit gracefully
		done := make(chan struct{})
		go func() {
			proc.Wait()
			close(done)
		}()
		select {
		case <-done:
		case <-time.After(5 * time.Second):
			// Force kill if it doesn't exit
			killProcessTree(proc.Pid)
		}
	}

	return resp.StatusCode == 202
}

// GetConnectionState returns the current connection state.
func (a *App) GetConnectionState() ConnectionState {
	a.mu.Lock()
	defer a.mu.Unlock()

	state := ConnectionState{
		Connected: a.connected,
		Attached:  a.attached,
		Output:    a.telaOutput,
	}

	if a.telaProcess != nil {
		state.Pid = a.telaProcess.Pid
	}

	// Load the saved profile to show connection details
	if a.connected {
		data, err := os.ReadFile(profilePath())
		if err == nil {
			var profile Profile
			if yaml.Unmarshal(data, &profile) == nil {
				state.Connections = profile.Connections
			}
		}
	}

	return state
}

// SetVerbose toggles verbose logging on the running tela instance via control API.
