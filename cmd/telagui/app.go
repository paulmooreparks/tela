// ShouldAutoConnect returns true if auto-connect is enabled and there's a saved profile.
func (a *App) ShouldAutoConnect() bool {
	settings := a.GetSettings()
	if !settings.AutoConnect {
		return false
	}
	// Check if there's a saved profile with connections
	connections := a.LoadProfile()
	return len(connections) > 0
}

// ShowWindow brings the window back from the system tray.
func (a *App) ShowWindow() {
	wailsRuntime.WindowShow(a.ctx)
	wailsRuntime.WindowSetAlwaysOnTop(a.ctx, true)
	wailsRuntime.WindowSetAlwaysOnTop(a.ctx, false)
}

// QuitApp initiates application exit. The actual confirmation and
// cleanup happen in OnBeforeClose.
func (a *App) QuitApp() {
	a.mu.Lock()
	a.quitting = true
	a.mu.Unlock()

	wailsRuntime.EventsEmit(a.ctx, "app:quitting")

	go func() {
		done := make(chan struct{})
		go func() {
			a.Disconnect()
			a.DisconnectControlWS()
			close(done)
		}()

		select {
		case <-done:
			// Clean disconnect succeeded
			wailsRuntime.Quit(a.ctx)
		case <-time.After(10 * time.Second):
			// Disconnect is stuck -- ask user
			wailsRuntime.EventsEmit(a.ctx, "app:quit-stuck")
			<-done // wait for user to force-kill or for it to finally finish
			wailsRuntime.Quit(a.ctx)
		}
	}()
}

// ForceQuit kills the tela and mount process trees and exits immediately.
func (a *App) ForceQuit() {
	mc := a.GetMountConfig()
	platformUnmountDrive(mc.Mount)

	a.mu.Lock()
	if a.mountProcess != nil {
		killProcessTree(a.mountProcess.Pid)
		a.mountProcess = nil
	}
	if a.telaProcess != nil {
		killProcessTree(a.telaProcess.Pid)
		a.telaProcess.Kill()
		a.telaProcess = nil
	}
	a.connected = false
	a.mu.Unlock()
	os.Exit(0)
}

// confirmQuit is called by OnBeforeClose when no confirmation is needed.
func (a *App) confirmQuit() {
	a.mu.Lock()
	a.quitting = true
	a.mu.Unlock()

	wailsRuntime.EventsEmit(a.ctx, "app:quitting")

	go func() {
		a.Disconnect()
		a.DisconnectControlWS()
		wailsRuntime.Quit(a.ctx)
	}()
}

